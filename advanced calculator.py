Var1 = int(input('Type here: '))
Var2 = int(input('Type here: '))
# int() means a number or integer, input() means to get the users input.
adding = input("Type here '+, -, *, /': ")
if adding == '+':
    print(Var1 + Var2)
if adding == '-':
    print(Var1 - Var2)
if adding == '*':
    print(Var1 * Var2)
if adding == '/':
    print(Var1 / Var2)

